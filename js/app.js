function abrirJogo(nome){

    switch(nome){

        case "memoria":

            window.location.href="jogos/memoria/index.html";

        break;

        case "cores":

            window.location.href="jogos/cores/index.html";

        break;

        case "emocoes":

            window.location.href="jogos/emocoes/index.html";

        break;

    }

}